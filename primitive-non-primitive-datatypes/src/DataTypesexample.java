public class DataTypesexample {

    public static void main(String[] args) {

        byte byteData = (byte) 10;
        short shortData = (short) 32767;
        int intData = 200000;
        float floatData = 10.1212121212f;
        long longData = 200000000l;
        double doubleData = 10.121212121212121215d;
        char charData = 'A';
        boolean bool = true;

        System.out.println("byte Min : "+ Byte.MIN_VALUE);
        System.out.println("byte  : "+byteData);
        System.out.println("byte Max : "+ Byte.MAX_VALUE);

        System.out.println("short Min :"+ Byte.MIN_VALUE);
        System.out.println("short  : "+shortData);
        System.out.println("short Max :"+ Byte.MAX_VALUE);

        System.out.println("int Min :"+ Byte.MIN_VALUE);
        System.out.println("int  : "+intData);
        System.out.println("int Max :"+ Byte.MAX_VALUE);

        System.out.println("float Min :"+ Byte.MIN_VALUE);
        System.out.println("float  : "+floatData);
        System.out.println("float Max :"+ Byte.MAX_VALUE);

        System.out.println("long Min :"+ Byte.MIN_VALUE);
        System.out.println("lomg  : "+longData);
        System.out.println("long Max :"+ Byte.MAX_VALUE);

        System.out.println("double Min :"+ Byte.MIN_VALUE);
        System.out.println("double  : "+doubleData);
        System.out.println("double Max :"+ Byte.MAX_VALUE);

        System.out.println("char Min :"+ Byte.MIN_VALUE);
        System.out.println("char  : "+charData);
        System.out.println("char Max :"+ Byte.MAX_VALUE);

        System.out.println("bool  : "+bool);

    }
}
